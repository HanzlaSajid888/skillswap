class UserProfile {
  final String id;
  final String name;
  final int age;
  final String photo;
  final String skillsTeach;
  final String skillsLearn;
  final int coins;

  UserProfile({
    required this.id,
    required this.name,
    required this.age,
    required this.photo,
    required this.skillsTeach,
    required this.skillsLearn,
    required this.coins,
  });
}
